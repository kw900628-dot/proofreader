import streamlit as st
import google.generativeai as genai

st.title("✨ 텍스트 교정기 (통합 버전)")
st.write("버튼 한 번에 맞춤법 검사와 윤문을 동시에 진행하여 API 한도를 절약합니다.")

# 사이드바: API 키 입력
with st.sidebar:
    st.header("설정")
    api_key = st.text_input("Gemini API Key를 입력하세요", type="password")

user_input = st.text_area("수정할 원본 글을 입력하세요:", height=200)

if st.button("교정 시작 (API 1회 호출)"):
    if not api_key or not user_input:
        st.warning("API Key와 원본 글을 모두 입력해 주세요.")
    else:
        try:
            genai.configure(api_key=api_key)
            # 할당량 초과 오류가 났던 2.5-flash 모델을 명시합니다. 
            # 만약 권한 오류가 나면 'gemini-pro'로 변경해 주세요.
            model = genai.GenerativeModel('gemini-2.5-flash')

            with st.spinner("국어원 어문 규범 적용 및 팀장님 스타일로 글을 재조립 중입니다..."):
                unified_prompt = f"""
                당신은 어문 규범을 완벽하게 숙지한 맞춤법 전문가이자, 문장 구조를 매끄럽게 재조립하는 수석 편집 팀장입니다.
                아래의 [1. 맞춤법 기준]과 [2. 구조적 윤문 원칙]을 동시에 적용하여 사용자의 글을 수정하고, 최종 결과물만 출력하세요.

                [1. 맞춤법 및 표준어 기준]
                - 국립국어원의 표준국어대사전을 기반으로 정확한 표준어를 사용하세요.
                - 국립국어원의 한국어 어문 규범을 바탕으로 띄어쓰기, 맞춤법, 문장 부호를 엄격하게 검사하고 교정하세요.

                [2. 구조적 윤문 원칙]
                1. 행동의 수식어화: 평면적으로 나열된 행동("A를 하고 B를 했다")을 관형절로 압축하여 대상을 꾸며주는 형태("A를 한 주어는 B를 했다")로 변환하세요.
                2. 파편화된 단문의 결합: 지나치게 짧게 끊어지는 문장들은 인과관계에 맞게 자연스러운 연결어미(~하여, ~고, ~어)로 묶어 성숙한 복문으로 만드세요.
                3. 주체 중심의 서술: 장소나 배경이 주어가 되는 헐거운 구조를 피하고, 문장을 이끄는 주체(사람 또는 핵심 대상)를 명확한 주어로 내세우세요.
                4. 문맥 중복 제거 (서술어 다이어트): 글의 전체 맥락상 이미 독자가 알고 있는 정보나 불필요하게 반복되는 단어는 생략하여 문장을 가볍게 만드세요.

                [팀장님 구조 수정 예시 (Few-Shot)]
                ▶ 예시 1) 행동의 수식어화
                - 원본 1: 소원이는 지구본을 빙그르르 돌려 보고는 깜짝 놀랐어요.
                - 수정 1: 지구본을 빙그르르 돌려 보던 소원이는 깜짝 놀랐어요.
                - 원본 2: 지구본에는 많은 나라가 그러져 있었거든요.
                - 수정 2: 지구본에 그려져 있는 나라가 무척 많았거든요.

                ▶ 예시 2) 파편화된 단문의 결합
                - 원본 1: 11월 말부터 독일 곳곳에서는 화려한 크리스마스 마켓이 열려요. 맛있는 먹거리와 장식용품 등을 살 수 있지요.
                - 수정 1: 독일에서는 11월 말부터 곳곳에서 화려한 크리스마스 마켓이 열려 맛있는 먹거리와 반짝이는 크리스마스 장식 등 다양한 물건을 살 수 있어요.
                - 원본 2: 마그마가 화산 폭발을 통해 땅 위로 흘러나오는 것을 용암이라고 해요. 마그마가 용암이 되어 밖으로 나오면, 안에 있던 가스들이 공기 중으로 대부분 빠져나가게 되지요.
                - 수정 2: 화산 활동이 일어나면 마그마가 땅 위로 흘러나와서 용암이 되어 흐르는데, 이때 마그마에 있던 가스들은 공기 중으로 대부분 빠져나가요.

                ▶ 예시 3) 주체 중심의 서술
                - 원본 1: 그래서 호주에서는 사람들이 가족이나 친구들과 함께 바닷가에서 산타 모자를 쓰고 놀거나...
                - 수정 1: 그래서 오스트레일리아 사람들은 크리스마스 때 가족이나 친구들과 바닷가에서 산타 모자를 쓰고 놀거나...
                - 원본 2: 1938년 멜버른에서 시작되어 많은 사람이 모여서 촛불을 켜고 다 함께 캐럴을 부르며 크리스마스를 축하하는 행사이지요.
                - 수정 2: 이 행사는 1938년 멜버른에서 시작되었는데, 많은 사람이 모여 촛불을 켜고 다 함께 캐럴을 부르며 크리스마스를 축하하는 행사이지요.

                ▶ 예시 4) 문맥 중복 제거
                - 원본 1: 거실에는 반짝반짝 예쁜 크리스마스트리가 장식되어 있고요. 크리스마스트리 아래에는 커다란 선물 상자가 놓여 있었어요.
                - 수정 1: 거실에는 반짝반짝 예쁘게 장식된 크리스마스트리가 있고, 크리스마스트리 아래에는 선물 상자들이 놓여 있어요.
                - 원본 2: 크리스마스이브에는 ‘캐럴스 바이 캔들라이트’라는 특별한 크리스마스 행사가 열려요.
                - 수정 2: 크리스마스이브에는 ‘캐럴스 바이 캔들라이트’라는 특별한 행사도 열어요.

                [원본 글]
                {user_input}

                [최종 팀장님 스타일 수정본]:
                """
                response = model.generate_content(unified_prompt)
                final_text = response.text.strip()

            st.subheader("💡 최종 교정 완료")
            st.success(final_text)

        except Exception as e:
            st.error(f"오류가 발생했습니다: {e}")