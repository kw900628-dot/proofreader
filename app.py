import streamlit as st
import google.generativeai as genai

st.title("✨ 팀장님 스타일 맞춤 교정기 (고도화 버전)")
st.write("1차 맞춤법/어문 규범 검사 ➔ 2차 팀장님 스타일 윤문을 순차적으로 진행합니다.")

# 사이드바: API 키 입력
with st.sidebar:
    st.header("설정")
    api_key = st.text_input("API KEY를 입력하세요", type="password")

user_input = st.text_area("수정할 원본 글을 입력하세요:", height=150)

if st.button("하이브리드 교정 시작"):
    if not api_key or not user_input:
        st.warning("API Key와 원본 글을 모두 입력해 주세요.")
    else:
        try:
            genai.configure(api_key=api_key)
            model = genai.GenerativeModel('gemini-1.5-flash')

            # ==========================================
            # [Step 2] 1차 필터: 국립국어원 어문 규범 기반 맞춤법 교정
            # ==========================================
            with st.spinner("1차: 국립국어원 어문 규범에 맞춰 맞춤법과 띄어쓰기를 교정 중입니다..."):
                grammar_prompt = f"""
                당신은 국립국어원의 표준국어대사전과 한국어 어문 규범을 엄격하게 준수하는 맞춤법 전문가입니다.
                다음 원본 글의 띄어쓰기, 맞춤법, 문장 부호, 표준어 여부를 검사하고 교정해 주세요.
                문맥이나 스타일은 절대 바꾸지 말고, 오직 '어문 규범'에 어긋난 부분만 수정하여 결과물 텍스트만 출력하세요.

                [원본 글]
                {user_input}
                """
                step1_response = model.generate_content(grammar_prompt)
                corrected_text = step1_response.text.strip()
            
            st.subheader("🔍 1차 교정 완료 (맞춤법/어문 규범 적용)")
            st.info(corrected_text)

            # ==========================================
            # [Step 1] 2차 필터: Few-Shot 기반 팀장님 스타일 윤문
            # ==========================================
            with st.spinner("2차: 1차 교정된 원고를 팀장님 스타일로 윤문 중입니다..."):
                style_prompt = f"""
		당신은 문장의 구조를 매끄럽고 논리적으로 재조립하는 데 탁월한 능력을 가진 수석 편집 팀장입니다.
		단순한 맞춤법 교정이나 단어 교체를 넘어, 아래의 4가지 '구조적 윤문 원칙'을 엄격하게 적용하여 사용자의 글을 수정하세요.

		[구조적 윤문 원칙]
		1. 행동의 수식어화: 평면적으로 나열된 행동("A를 하고 B를 했다")을 관형절로 압축하여 대상을 꾸며주는 형태("A를 한 주어는 B를 했다")로 변환하세요.
		2. 파편화된 단문의 결합: 지나치게 짧게 끊어지는 문장들은 상황과 결과, 인과관계에 맞게 자연스러운 연결어미(~하여, ~고, ~어)로 묶어 성숙한 복문으로 만드세요.
		3. 주체 중심의 서술: 장소나 배경이 주어가 되는 헐거운 구조를 피하고, 문장을 이끄는 주체(사람 또는 핵심 대상)를 명확한 주어로 내세우세요.
		4. 문맥 중복 제거 (서술어 다이어트): 글의 전체 맥락상 이미 독자가 알고 있는 정보나 불필요하게 반복되는 단어는 과감하게 생략하여 문장을 가볍게 만드세요.

		[팀장님 구조 수정 예시 (Few-Shot)]

		▶ 예시 1) 행동의 수식어화
		- 원본 1: 소원이는 지구본을 빙그르르 돌려 보고는 깜짝 놀랐어요[cite: 1].
		- 수정 1: 지구본을 빙그르르 돌려 보던 소원이는 깜짝 놀랐어요[cite: 1].
		- 원본 2: 지구본에는 많은 나라가 그러져 있었거든요[cite: 1].
		- 수정 2: 지구본에 그려져 있는 나라가 무척 많았거든요[cite: 1].

		▶ 예시 2) 파편화된 단문의 결합
		- 원본 1: 11월 말부터 독일 곳곳에서는 화려한 크리스마스 마켓이 열려요. 맛있는 먹거리와 장식용품 등을 살 수 있지요[cite: 1].
		- 수정 1: 독일에서는 11월 말부터 곳곳에서 화려한 크리스마스 마켓이 열려 맛있는 먹거리와 반짝이는 크리스마스 장식 등 다양한 물건을 살 수 있어요[cite: 1].
		- 원본 2: 마그마가 화산 폭발을 통해 땅 위로 흘러나오는 것을 용암이라고 해요. 마그마가 용암이 되어 밖으로 나오면, 안에 있던 가스들이 공기 중으로 대부분 빠져나가게 되지요[cite: 1].
		- 수정 2: 화산 활동이 일어나면 마그마가 땅 위로 흘러나와서 용암이 되어 흐르는데, 이때 마그마에 있던 가스들은 공기 중으로 대부분 빠져나가요[cite: 1].

		▶ 예시 3) 주체 중심의 서술
		- 원본 1: 그래서 호주에서는 사람들이 가족이나 친구들과 함께 바닷가에서 산타 모자를 쓰고 놀거나...[cite: 1]
		- 수정 1: 그래서 오스트레일리아 사람들은 크리스마스 때 가족이나 친구들과 바닷가에서 산타 모자를 쓰고 놀거나...[cite: 1]
		- 원본 2: 1938년 멜버른에서 시작되어 많은 사람이 모여서 촛불을 켜고 다 함께 캐럴을 부르며 크리스마스를 축하하는 행사이지요[cite: 1].
		- 수정 2: 이 행사는 1938년 멜버른에서 시작되었는데, 많은 사람이 모여 촛불을 켜고 다 함께 캐럴을 부르며 크리스마스를 축하하는 행사이지요[cite: 1]. (숨어있던 주어 '이 행사는' 명시)

		▶ 예시 4) 문맥 중복 제거
		- 원본 1: 거실에는 반짝반짝 예쁜 크리스마스트리가 장식되어 있고요. 크리스마스트리 아래에는 커다란 선물 상자가 놓여 있었어요[cite: 1].
		- 수정 1: 거실에는 반짝반짝 예쁘게 장식된 크리스마스트리가 있고, 크리스마스트리 아래에는 선물 상자들이 놓여 있어요[cite: 1].
		- 원본 2: 크리스마스이브에는 ‘캐럴스 바이 캔들라이트’라는 특별한 크리스마스 행사가 열려요[cite: 1].
		- 수정 2: 크리스마스이브에는 ‘캐럴스 바이 캔들라이트’라는 특별한 행사도 열어요[cite: 1]. (전체 주제인 '크리스마스' 단어 중복 제거)

		[1차 교정된 글]
		{corrected_text}

		[최종 팀장님 스타일 수정본]:
		"""
                step2_response = model.generate_content(style_prompt)
                final_text = step2_response.text.strip()

            st.subheader("💡 2차 교정 완료 (팀장님 스타일 적용)")
            st.success(final_text)

        except Exception as e:
            st.error(f"오류가 발생했습니다: {e}")